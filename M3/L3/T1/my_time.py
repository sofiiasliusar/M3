import my_time
# Функція для переведення хвилин у секунди
# Вхідні параметри: m - кількість секунд
# Повернене значення: кількість секунд у хвилинах


def seconds_in_min(m):
    return m*60


# Функція  для переведення хвилин у секунди
# Вхідні параметри: h - кількість годин
# Повернене значення: кількість секунд у хвилинах


def seconds_in_hour(h):
    return h*3600
